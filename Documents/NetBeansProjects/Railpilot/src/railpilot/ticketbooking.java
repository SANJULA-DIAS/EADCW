private String generateTicketNumber() {
    String latestTicketNumber = "P1"; // Default if no tickets are found

    // Connect to the database and fetch the latest ticket number
    String dbloc = "jdbc:mysql://localhost:3306/railpilot";
    String username = "root";

    try (Connection connection = DriverManager.getConnection(dbloc, username, "")) {
        String query = "SELECT MAX(PassengerTicket_Id) FROM Passenger";
        try (PreparedStatement preparedStatement = connection.prepareStatement(query)) {
            try (ResultSet resultSet = preparedStatement.executeQuery()) {
                if (resultSet.next()) {
                    String maxTicketNumber = resultSet.getString(1);
                    if (maxTicketNumber != null) {
                        int ticketNumber = Integer.parseInt(maxTicketNumber.substring(1));
                        latestTicketNumber = "P" + (ticketNumber + 1);
                    }
                }
            }
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, "Error connecting to the database: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }

    return latestTicketNumber;
}

    
    
    

