    private void jLabel9MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel9MouseClicked
        String ticketNumber = txtticketNumber.getText();
        String name = txtname.getText();
        String email = txtemail.getText();
        String telephone = txttelephone.getText();
        String cost = txtCost.getText();

        if (ticketNumber.isEmpty() || name.isEmpty() || email.isEmpty() || telephone.isEmpty() || cost.isEmpty()) {
            JOptionPane.showMessageDialog(null, "All fields must be filled", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!isValidEmail(email)) {
            JOptionPane.showMessageDialog(null, "Invalid email format", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!isValidTelephone(telephone)) {
            JOptionPane.showMessageDialog(null, "Invalid telephone number format", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String dbloc = "jdbc:mysql://localhost:3306/railpilot";
        String username = "root";

        try (Connection connection = DriverManager.getConnection(dbloc, username, "")) {
            String query = "INSERT INTO Passenger (PassengerTicket_Id, Passenger_Name, Departure, Destination, Cost, Email, Passenger_TP) VALUES (?, ?, ?, ?, ?, ?, ?)";
            try (PreparedStatement preparedStatement = connection.prepareStatement(query)) {
                preparedStatement.setString(1, ticketNumber);
                preparedStatement.setString(2, name);
                preparedStatement.setString(3, (String) fromcombobox.getSelectedItem()); // Get the selected text from "From" combo box
                preparedStatement.setString(4, (String) tocombobox.getSelectedItem());   // Get the selected text from "To" combo box
                preparedStatement.setInt(5, Integer.parseInt(cost));
                preparedStatement.setString(6, email);
                preparedStatement.setInt(7, Integer.parseInt(telephone));

                int rowsInserted = preparedStatement.executeUpdate();
                if (rowsInserted > 0) {
                    JOptionPane.showMessageDialog(null, "Ticket booked successfully");
                } else {
                    JOptionPane.showMessageDialog(null, "Failed to book ticket", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error connecting to the database: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

        String tiketNumberText = txtticketNumber.getText().substring(1);

        int previousTicketID =0;
        boolean success = false;

        try {
            previousTicketID = Integer.parseInt(tiketNumberText);
            success = true;
        } catch (NumberFormatException e) {
            // Handle the case where parsing fails
            e.printStackTrace();
        }

        // If the previous train ID could be parsed successfully, increment it
        if (success) {
            int nextTrainID = previousTicketID + 1;
            txtticketNumber.setText("P" + nextTrainID);
        } else {
            JOptionPane.showMessageDialog(null, "Something's wrong, please check again", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jLabel9MouseClicked

    
    
