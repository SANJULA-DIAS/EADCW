           private void sendEmail() {
    final String username = "railpilot@outlook.com";
    final String password = "TRAILRESERVER#";

    Properties props = new Properties();
    props.put("mail.smtp.auth", "true");
    props.put("mail.smtp.starttls.enable", "true");
    props.put("mail.smtp.host", "smtp.office365.com");  // Use the appropriate SMTP server for Outlook
    props.put("mail.smtp.port", "587");

    Session session = Session.getInstance(props, new Authenticator() {
        protected PasswordAuthentication getPasswordAuthentication() {
            return new PasswordAuthentication(username, password);
        }
    });

    try {
        Message message = new MimeMessage(session);
        message.setFrom(new InternetAddress(username));
        message.setRecipients(Message.RecipientType.TO, InternetAddress.parse(txtemail.getText()));
        message.setSubject(txtticketNumber.getText()+" Ticket Deleted");
        message.setText("Dear,\n\nYour ticket "+txtticketNumber.getText()+ " has been successfully deleted.");

        Transport.send(message);

        System.out.println("Email sent successfully");

    } catch (MessagingException e) {
        throw new RuntimeException(e);
    }
}

    
    

