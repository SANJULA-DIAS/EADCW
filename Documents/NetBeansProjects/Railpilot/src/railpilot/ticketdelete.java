    private void jLabel9MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel9MouseClicked
        String ticketNumber = txtticketNumber.getText();
    String email = txtemail.getText();
  
    if (ticketNumber.isEmpty() || email.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Enter both Ticket Number and Email", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    String dbloc = "jdbc:mysql://localhost:3306/railpilot";
    String username = "root";

    try (Connection connection = DriverManager.getConnection(dbloc, username, "")) {
        String query = "DELETE FROM Passenger WHERE PassengerTicket_Id=? AND Email=?";
    
        try (PreparedStatement preparedStatement = connection.prepareStatement(query)) {
            preparedStatement.setString(1, txtticketNumber.getText());
            preparedStatement.setString(2, txtemail.getText());

            int rowsDeleted = preparedStatement.executeUpdate();
            if (rowsDeleted > 0) {
                
                JOptionPane.showMessageDialog(null, "Ticket deleted successfully");
                sendEmail();
                txtticketNumber.setText("");
    txtemail.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "Failed to delete ticket. Ticket not found or email does not match.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    } catch (SQLException e) {
        e.printStackTrace();
    }


    }//GEN-LAST:event_jLabel9MouseClicked

    
    
    
    
    
